import { Entity, PrimaryGeneratedColumn, Column } from 'typeorm';
import { UserRole } from '@/common/enums/global.emun';
@Entity()
export class User {
  @PrimaryGeneratedColumn()
  id: number;

  @Column({ unique: true })
  username: string;

  @Column()
  password: string;

  @Column({
    type: 'enum',
    enum: UserRole,
    default: UserRole.USER,
  })
  role: UserRole;
}
